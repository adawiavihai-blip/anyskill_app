  /// Returns the best answer for [question].
  /// Tries exact match first, then keyword overlap, then a generic fallback.
  static String findAnswer(String question) {
    // 1. Exact match
    if (_answers.containsKey(question)) return _answers[question]!;

    // 2. Keyword overlap — best-matching key wins
    final q = question.toLowerCase();
    String? bestKey;
    int bestScore = 0;

    for (final key in _answers.keys) {
      final words = key.toLowerCase().split(' ');
      final score = words.where((w) => w.length > 1 && q.contains(w)).length;
      if (score > bestScore) {
        bestScore = score;
        bestKey = key;
      }
    }

    if (bestKey != null && bestScore > 0) return _answers[bestKey]!;

    // 3. Generic fallback
    return '🤔 שאלה מצוינת!\n\n'
        'לא מצאתי תשובה מדויקת, אבל צוות התמיכה שלנו כאן בשבילך.\n\n'
        '📧 פנו אלינו: support@anyskill.app\n'
        '⏱️ זמן תגובה ממוצע: עד שעתיים\n\n'
        'תוכלו גם לנסות לנסח את השאלה מחדש או לבחור אחת מהשאלות הנפוצות למטה.';
  }
